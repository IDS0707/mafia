let currentLang = (localStorage.getItem(STORAGE) || (() => {
  const nav = (navigator.language || 'en').toLowerCase();
  if (nav.startsWith('uz')) return 'uz';
  if (nav.startsWith('ru')) return 'ru';
  return 'en';
})());

function interp(str, vars) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
}

function t(key, vars) {
  const dict = DICT[currentLang] || DICT.en;
  const fallback = DICT.en;
  const raw = (key in dict) ? dict[key] : (fallback[key] !== undefined ? fallback[key] : key);
  return interp(raw, vars);
}

function applyDOM(root) {
  const scope = root || document;
  scope.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  scope.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });
  scope.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    el.setAttribute('placeholder', t(key));
  });
  scope.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.setAttribute('title', t(key));
  });
}

function setLang(code) {
  if (!DICT[code]) code = 'en';
  currentLang = code;
  localStorage.setItem(STORAGE, code);
  document.documentElement.lang = code;
  applyDOM();
  // Notify listeners (so app.js can re-render dynamic views)
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang: code } }));
  // Sync switcher pills
  document.querySelectorAll('[data-lang-pill]').forEach(p => {
    p.classList.toggle('active', p.getAttribute('data-lang-pill') === code);
  });
}

function getLang() { return currentLang; }

window.t = t;
window.setLang = setLang;
window.getLang = getLang;

// Apply on initial DOM ready
document.addEventListener('DOMContentLoaded', () => {
  document.documentElement.lang = currentLang;
  applyDOM();
  document.querySelectorAll('[data-lang-pill]').forEach(p => {
    p.classList.toggle('active', p.getAttribute('data-lang-pill') === currentLang);
    p.addEventListener('click', () => setLang(p.getAttribute('data-lang-pill')));
  });
});

